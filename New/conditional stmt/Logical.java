package New;

public class Logical {
    public static void main(String []args){
        //Logical AND (&&) = TRUE only when both statements are true otherwise false
        System.out.println((5 > 4) && (4>2));

        //Logical OR (||)= FALSE only when both the statements are false otherwise if only one statement is false it returns true
        System.out.println((5>4) || (10>23));
    }
}
